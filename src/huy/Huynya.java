package huy;

public class Huynya {
    private String l;
    private String o;
    private String x;

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }

    public String getO() {
        return o;
    }

    public void setO(String o) {
        this.o = o;
    }

    public Huynya(String l, String o, String x) {
        this.l = l;
        this.o = o;
        this.x = x;
    }
}